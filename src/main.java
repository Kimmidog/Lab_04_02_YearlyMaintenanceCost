public class main {
    public static void main(String[] args){
        double springCost = 550.00;
        double summerCost = 650.00;
        double fallCost = 750.00;
        double winterCost = 850.00;
        double totalCost = 0;

        totalCost = springCost + summerCost + fallCost + winterCost;

        System.out.println("The cost for spring was " + springCost);
        System.out.println("The cost for summer was " + summerCost);
        System.out.println("The cost for fall was " + fallCost);
        System.out.println("The cost for winter was " + winterCost);
        System.out.println("the total yearly cost was " + totalCost);
    }
}
